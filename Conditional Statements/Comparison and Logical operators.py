# Comparison Operators in Conditions
# Conditions often use comparison operators.

x = 10
 
if x == 10:
    print("Equal")
 
if x != 5:
    print("Not equal")

# Logical Operators in Conditions
# Logical operators allow combining multiple conditions.

age = 25
country = "India"
 
if age >= 18 and country == "India":
    print("Allowed")

