# The else Statement
# The else block runs when the if condition is false.


age = 16
if age >= 18:
    print("Eligible")
else:
    print("Not eligible")




# Using pass in Conditionals
# The pass statement is used when a condition is required syntactically but no action is needed or when we want to write if condition after writing else

age = 15
 
if age >= 18:
    pass
else:
    print("Minor")